namespace GunsAmerica.Models;

/// <summary>
/// The order in which the results are returned.
/// </summary>
public enum ItemSort
{
    /// <summary>
    /// Item number ASC.
    /// </summary>
    ItemNumberAsc = 2,

    /// <summary>
    /// Item number DESC.
    /// </summary>
    ItemNumberDesc = 3,

    /// <summary>
    /// Item price ASC.
    /// </summary>
    ItemPriceAsc = 4,

    /// <summary>
    /// Item number DESC.
    /// </summary>
    ItemPriceDesc = 5,

    /// <summary>
    /// Starting date ASC.
    /// </summary>
    StartingDateAsc = 6,

    /// <summary>
    /// Starting date DESC.
    /// </summary>
    StartingDateDesc = 7,
}
